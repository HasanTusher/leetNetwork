package com.leet.learn;

public class Result {
    int cost;
    int iteration;

    public int getCost() {
        return cost;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }

    public int getIteration() {
        return iteration;
    }

    public void setIteration(int iteration) {
        this.iteration = iteration;
    }

    @Override
    public String toString() {
        return "Result{" +
                "cost=" + cost +
                ", iteration=" + iteration +
                '}';
    }
}
